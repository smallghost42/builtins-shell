#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*str;

	str = (char *) malloc((ft_strlen(s) + 1) * sizeof(char));
	i = 0;
	if (!str)
		return (0);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
